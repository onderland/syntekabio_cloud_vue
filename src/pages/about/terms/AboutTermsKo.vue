                    2. 회원은 멤버의 초대 여부 및 멤버의 이용권한의 범위를
                    자유롭게 결정할 수 있으며,이로 인한 회원과 멤버 간 또는
                    회원과 제 3 자 간 분쟁 발생 시 회사는 책임을 지지 않습니다.
                  </li>
                  <li>
                    3. 슈퍼관리자는 허가된 권한의 범위 안에서 멤버의 데이터에
                    접근할 수 있습니다. 단, 회원은 멤버가 STB 클라우드 계정
                    이용을 시작하기 전에 멤버로부터 슈퍼관리자가 해당 멤버의
                    데이터에 접근할 수 있고 데이터를 모니터링, 이용 또는 공개할
                    수 있다는 점에 대하여 동의를 받아야 합니다.
                  </li>
                  <li>
                    4. 본 조 제3항에 반하여 멤버의 일부 또는 전부가 동의를 하지
                    않는 경우에도 이로 인한 회원과 멤버 간 분쟁 발생 시 회사는
                    책임을 지지 않습니다.
                  </li>
                </ul>
              </dl>

