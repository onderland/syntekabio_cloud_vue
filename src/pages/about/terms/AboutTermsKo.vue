                      허용되는 한도 내에서 간접 손해, 특별 손해, 결과적 손해,
                      징계적 손해, 및 징벌적 손해에 대한 책임을 부담하지
                      않습니다.
                    </li>
                    <ul class="ul-terms">
                      <li>
                        ① 무료서비스, 테스트, 시범 운영 서비스의 이용으로 인한
                        손해
                      </li>
                      <li>
                        ② 회원간, 회원과 멤버간, 멤버간 또는 멤버와 제 3자간에
