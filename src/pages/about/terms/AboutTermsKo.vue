                    이전에 본 약관 및 서비스 계약의 조건들을 완전히 이행할
                    책임이 있습니다. 승계와 관련하여 문제가 발생하는 경우,
                    회원과 양수인이 연대하여 책임을 집니다.
                  </li>
                </ul>
              </dl>

