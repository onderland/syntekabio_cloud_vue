              <dl class="dl-terms">
                <dt class="title">제 20 조 (권리의 귀속)</dt>
                <ul class="ul-terms">
                  <li>
                    1. STB 클라우드 서비스 및 개별 플랫폼에 사용되는
                    소프트웨어를 포함하는 사이트 전반에 대한 저작권 등
                    지식재산권은 회사에 귀속됩니다.
                  </li>
                  <li>
                    2. 회사는 회원에게 본 약관을 포함하여 회사가 사전에 정하여
                    공지 또는 통지한 이용조건에 따라 STB 클라우드 서비스 및 개별
                    플랫폼을 이용할 수 있는 이용권만을 부여하며, 회원은 이를
                    양도 또는 판매하거나, 담보로 제공하는 등 처분행위를 하여서는
                    안됩니다.
                  </li>
                  <li>
                    3. STB 클라우드 서비스의 합법적 이용에 따른 회원의 프로젝트
                    결과물의 소유권은 회원에 있으며, 회사는 이에 대한 소유권
                    또는 지식재산권을 주장하지 않습니다.
                  </li>
                </ul>
              </dl>

              <!-- 제 21 조 (권리 불포기 조항) -->
              <dl class="dl-terms">
                <dt class="title">제 21 조 (권리 불포기 조항)</dt>
                <dd class="text">
                  회원의 본 약관 또는 세부지침의 위반과 관련하여 회사가 권리
