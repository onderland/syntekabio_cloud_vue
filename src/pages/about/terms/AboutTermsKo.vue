                    등으로 할 수 있습니다. 회사는 7일 이상 STB 클라우드 사이트
                    또는 STB 클라우드 서비스에 공지 등을 게시함으로써 회원
                    전체에 대한 통지를 대신할 수 있습니다.
                  </li>
                  <li>
                    2. 회사가 슈퍼관리자에게 통지한 경우 이는 회원에게
                    통지한것으로 봅니다. 회원은 슈퍼관리자 정보에 대한 관리 및
                    적절한 정보로의 변경 책임을 부담합니다.
                  </li>
                </ul>
              </dl>

